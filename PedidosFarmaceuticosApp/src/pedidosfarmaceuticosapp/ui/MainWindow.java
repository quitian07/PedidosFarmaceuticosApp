    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        try {
        // Validaciones
        String medicineName = txtMedicinename.getText().trim();
        if (medicineName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del medicamento no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!medicineName.matches("[a-zA-Z0-9 ]+")) {
            JOptionPane.showMessageDialog(this, "El nombre del medicamento solo puede contener caracteres alfanuméricos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String medicineType = (String) cbMedicineType.getSelectedItem();
        if (medicineType.equals("Seleccione")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de medicamento", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String quantityText = txtQuantity.getText().trim();
        if (quantityText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La cantidad no puede estar vacía", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int quantity;
        try {
            quantity = Integer.parseInt(quantityText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser un número entero", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (quantity <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser un número positivo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String distributor = null;
        if (rbCofarma.isSelected()) {
            distributor = "Cofarma";
        } else if (rbEmpsephar.isSelected()) {
            distributor = "Empsephar";
        } else if (rbCemefar.isSelected()) {
            distributor = "Cemefar";
        }
        if (distributor == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un distribuidor", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!chkPrincipal.isSelected() && !chkSecundaria.isSelected()) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar al menos una sucursal", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear el pedido
        Order order = new Order();
        order.setMedicineName(medicineName);
        order.setMedicineType(medicineType);
        order.setQuantity(quantity);
        order.setDistributor(distributor);
        order.setMainBranch(chkPrincipal.isSelected());
        order.setSecondaryBranch(chkSecundaria.isSelected());

        // Abrir ventana de confirmación
        ConfirmationWindow confirmationWindow = new ConfirmationWindow(order);
        confirmationWindow.setVisible(true);
        this.dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnConfirmarActionPerformed

